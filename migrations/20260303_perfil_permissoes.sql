-- ============================================================================
-- 1. TABELA perfil_permissoes
-- ============================================================================
CREATE TABLE IF NOT EXISTS perfil_permissoes (
  id BIGSERIAL PRIMARY KEY,
  perfil VARCHAR(30) NOT NULL,
  menu_key TEXT NOT NULL,
  pode_visualizar BOOLEAN NOT NULL DEFAULT FALSE,
  pode_editar BOOLEAN NOT NULL DEFAULT FALSE,
  pode_excluir BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT uq_perfil_permissoes UNIQUE (perfil, menu_key),
  CONSTRAINT ck_perfil_permissoes_perfil CHECK (
    perfil IN (
      'super_admin',
      'coordenador',
      'secretaria',
      'professor',
      'gestor_rh',
      'admin_curso'
    )
  )
);

COMMENT ON TABLE perfil_permissoes IS 'Mapa de permissões de acesso às seções do painel admin por perfil de usuário.
   menu_key corresponde à propriedade key de cada item em menuConfig.tsx.
   O perfil é derivado de tipo_pessoa (super_admin/coordenador/secretaria/professor)
   ou de lms_empresa_usuarios.perfil (gestor_rh/admin_curso).';

COMMENT ON COLUMN perfil_permissoes.menu_key IS 'Chave do item de menu (menuConfig.tsx key)';

COMMENT ON COLUMN perfil_permissoes.pode_visualizar IS 'Item aparece no menu / rota carrega';

COMMENT ON COLUMN perfil_permissoes.pode_editar IS 'Usuário pode criar e atualizar registros nessa tela';

COMMENT ON COLUMN perfil_permissoes.pode_excluir IS 'Usuário pode excluir/cancelar registros nessa tela';

CREATE INDEX IF NOT EXISTS idx_perfil_permissoes_perfil ON perfil_permissoes(perfil);

CREATE INDEX IF NOT EXISTS idx_perfil_permissoes_menu_key ON perfil_permissoes(menu_key);

-- Trigger de updated_at
CREATE TRIGGER trg_perfil_permissoes_set_updated_at BEFORE
UPDATE
  ON perfil_permissoes FOR EACH ROW EXECUTE FUNCTION fn_lms_set_updated_at();

