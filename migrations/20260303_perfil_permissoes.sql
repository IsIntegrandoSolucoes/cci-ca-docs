CREATE
OR REPLACE FUNCTION fn_get_permissoes_perfil(p_perfil TEXT) RETURNS TABLE (
  menu_key TEXT,
  pode_visualizar BOOLEAN,
  pode_editar BOOLEAN,
  pode_excluir BOOLEAN
) LANGUAGE SQL STABLE SECURITY DEFINER
SET
  search_path = public AS $ $
SELECT
  menu_key,
  pode_visualizar,
  pode_editar,
  pode_excluir
FROM
  perfil_permissoes
WHERE
  perfil = p_perfil
  AND pode_visualizar = TRUE
ORDER BY
  menu_key;

$ $;

